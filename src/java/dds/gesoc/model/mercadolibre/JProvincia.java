package dds.gesoc.model.mercadolibre;

public class JProvincia {
	
	private String id;
	private String name;
	
	public JProvincia(String id, String name) {
		this.id = id;
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public String getName() {
		return name;
	}
}
